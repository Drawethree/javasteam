public class SupportInfo {
    private String url;
    private String email;

    public SupportInfo() { }

    /**
     * Support Url
     * @return Url as String
     */
    public String getUrl() {
        return url;
    }

    /**
     * Support email
     * @return Email as String
     */
    public String getEmail() {
        return email;
    }
}
